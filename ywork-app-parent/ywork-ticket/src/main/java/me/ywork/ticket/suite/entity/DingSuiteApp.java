package me.ywork.ticket.suite.entity;

import me.ywork.base.entity.Entity;

/**
 * 套件内应用
 * 
 * @author
 * @version 1.0 2015-07-27
 */
public class DingSuiteApp extends Entity {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6845755008837044334L;

	/**
	 * 应用ID
	 */
	protected String appId;
	
	/**
	 * 套件ID
	 */
	
	protected String suiteId;
	
	protected String appName;
	
	protected String specialPermission;
	

	public String getAppId() {
		return appId;
	}

	public void setAppId(String appId) {
		this.appId = appId;
	}
	
	public String getSuiteId() {
		return suiteId;
	}

	public void setSuiteId(String suiteId) {
		this.suiteId = suiteId;
	}

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public String getSpecialPermission() {
		return specialPermission;
	}

	public void setSpecialPermission(String specialPermission) {
		this.specialPermission = specialPermission;
	}
	
}
