package me.ywork.ticket.suite.model;

import java.io.Serializable;
import java.util.List;


public class QySuiteAuthInfo implements Serializable{
	public QySuiteAuthInfo() {
	}
	private List<QySuiteAgent> qySuiteAgents;
	private List<QySuiteDepartment> QySuiteDepartments;
	public List<QySuiteAgent> getQySuiteAgents() {
		return qySuiteAgents;
	}
	public void setQySuiteAgents(List<QySuiteAgent> qySuiteAgents) {
		this.qySuiteAgents = qySuiteAgents;
	}
	public List<QySuiteDepartment> getQySuiteDepartments() {
		return QySuiteDepartments;
	}
	public void setQySuiteDepartments(List<QySuiteDepartment> qySuiteDepartments) {
		QySuiteDepartments = qySuiteDepartments;
	}
}
