package me.ywork.ticket.suite.model;

public class QySuitePermanentCode extends DingReceiveBaseModel{ 
	
	private String accessToken;
	private int expiresIn;
	private String permanentCode;
	private QySuiteAuthCorpInfo authCorpInfo;
	private QySuiteAuthInfo authInfo;
	
	private String authEmail; //授权管理员的邮箱，可能为空
	private String authMobile; //授权管理员的手机号，可能为空
	
	
	public String getAuthEmail() {
		return authEmail;
	}

	public void setAuthEmail(String authEmail) {
		this.authEmail = authEmail;
	}

	public String getAuthMobile() {
		return authMobile;
	}

	public void setAuthMobile(String authMobile) {
		this.authMobile = authMobile;
	}

	public String getAccessToken() {
		return accessToken;
	}

	public void setAccessToken(String accessToken) {
		this.accessToken = accessToken;
	}

	public int getExpiresIn() {
		return expiresIn;
	}

	public void setExpiresIn(int expiresIn) {
		this.expiresIn = expiresIn;
	}

	public String getPermanentCode() {
		return permanentCode;
	}

	public void setPermanentCode(String permanentCode) {
		this.permanentCode = permanentCode;
	}

	

	public QySuiteAuthCorpInfo getAuthCorpInfo() {
		return authCorpInfo;
	}

	public void setAuthCorpInfo(QySuiteAuthCorpInfo authCorpInfo) {
		this.authCorpInfo = authCorpInfo;
	}

	public QySuiteAuthInfo getAuthInfo() {
		return authInfo;
	}

	public void setAuthInfo(QySuiteAuthInfo authInfo) {
		this.authInfo = authInfo;
	}

	public QySuitePermanentCode(){
		
	}
}
