package me.ywork.suite.api.model;

import java.io.Serializable;

public class CorpAdminRpcModel implements Serializable{
	
	private String corpId;
	private String corpName;
	private String userId;
	private String userName;
	private boolean isAdmin;
	private boolean isSupAdmin;
	private boolean isBoss;
	private String avatar;
	
	
	public String getCorpName() {
		return corpName;
	}
	public void setCorpName(String corpName) {
		this.corpName = corpName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getCorpId() {
		return corpId;
	}
	public void setCorpId(String corpId) {
		this.corpId = corpId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public boolean isAdmin() {
		return isAdmin;
	}
	public void setAdmin(boolean isAdmin) {
		this.isAdmin = isAdmin;
	}
	public boolean isSupAdmin() {
		return isSupAdmin;
	}
	public void setSupAdmin(boolean isSupAdmin) {
		this.isSupAdmin = isSupAdmin;
	}
	public boolean isBoss() {
		return isBoss;
	}
	public void setBoss(boolean isBoss) {
		this.isBoss = isBoss;
	}
	public String getAvatar() {
		return avatar;
	}
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	
	
}
