package me.ywork.ticket.message;

import com.heyi.framework.messagebus.message.DefaultMessage;

/**
 * 钉钉企业通讯录发生变化事件
 * @author V
 *
 */
public class DingOrgModifyMessage extends DefaultMessage{
	/**
	 * 
	 */
	private static final long serialVersionUID = 3619896462548171054L;

	private String eventType;
	
	private String suiteId;
	
	private String corpId;
	
	private String[] deptId;
	
	private String[] userId;

	public String getEventType() {
		return eventType;
	}

	public void setEventType(String eventType) {
		this.eventType = eventType;
	}

	public String getCorpId() {
		return corpId;
	}

	public void setCorpId(String corpId) {
		this.corpId = corpId;
	}
		
	public String getSuiteId() {
		return suiteId;
	}

	public void setSuiteId(String suiteId) {
		this.suiteId = suiteId;
	}
	
	public String[] getDeptId() {
		return deptId;
	}

	public void setDeptId(String[] deptId) {
		this.deptId = deptId;
	}

	public String[] getUserId() {
		return userId;
	}

	public void setUserId(String[] userId) {
		this.userId = userId;
	}
}
