package me.ywork.salarybill.entity;

import me.ywork.base.entity.Entity;

/**
 * 薪资条主表
 */
public class SalaryBillSms extends Entity {

	private static final long serialVersionUID = -1581975887830680505L;

	/**
	 * 企业号
	 */
	private String companyId;
	
	/**
	 * 员工编号
	 */
	private String userId;
	
	/**
	 * 短信总计
	 */
	private int total;
	
	/**
	 * 已使用统计
	 */
	private int usedCount;
	
	/**
	 * 是否启用短信功能
	 */
	private boolean enable;
	
	/**
	 * 短信发送规则
	 */
	private Short smsMode;
	
	/**
	 * 显示规则
	 */
	private Short smsShow;
	
	
	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public int getUsedCount() {
		return usedCount;
	}

	public void setUsedCount(int usedCount) {
		this.usedCount = usedCount;
	}

	public boolean isEnable() {
		return enable;
	}

	public void setEnable(boolean enable) {
		this.enable = enable;
	}

	public Short getSmsMode() {
		return smsMode;
	}

	public void setSmsMode(Short smsMode) {
		this.smsMode = smsMode;
	}

	public Short getSmsShow() {
		return smsShow;
	}

	public void setSmsShow(Short smsShow) {
		this.smsShow = smsShow;
	}
	
}
