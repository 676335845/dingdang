package me.ywork.salarybill.entity;

import java.util.Date;

import me.ywork.base.entity.Entity;

/**
 * 用于员工查看自己薪资条和后台管理时使用的密码数据
 * 
 */
public class SalaryBillPwdSet extends Entity {

	private static final long serialVersionUID = -8054645190067601935L;
	/**
	 * 企业ID
	 */
	private String companyId;
	/**
	 * 员工编号
	 */
	private String userId;
	/**
	 * 密码类型
	 */
	private Short passwordType;

	/**
	 * dingding随机码
	 */
	private Integer dingRandom;

	/**
	 * 密码
	 */
	private String password;
	/**
	 * 是否需要重置
	 */
	private Boolean needReset;
	/**
	 * 创建人员
	 */
	private String createUserId;
	/**
	 * 创建时间
	 */
	private Date createTime;
	/**
	 * 更新人员
	 */
	private String updateUserId;
	/**
	 * 更新时间
	 */
	private Date updateTime;

	/**
	 * 输入错误次数
	 */
	private Integer errPwdCount;

	/**
	 * 禁用再次开放的时间
	 */
	private Date releaseTime;

	/**
	 * 查看数据是否需要密码
	 */
	private Boolean isNeedPasswd;

	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Short getPasswordType() {
		return passwordType;
	}

	public void setPasswordType(Short passwordType) {
		this.passwordType = passwordType;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public Boolean getNeedReset() {
		return needReset;
	}

	public void setNeedReset(Boolean needReset) {
		this.needReset = needReset;
	}

	public String getCreateUserId() {
		return createUserId;
	}

	public void setCreateUserId(String createUserId) {
		this.createUserId = createUserId;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public String getUpdateUserId() {
		return updateUserId;
	}

	public void setUpdateUserId(String updateUserId) {
		this.updateUserId = updateUserId;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	public Integer getDingRandom() {
		return dingRandom;
	}

	public void setDingRandom(Integer dingRandom) {
		this.dingRandom = dingRandom;
	}

	public Integer getErrPwdCount() {
		return errPwdCount;
	}

	public void setErrPwdCount(Integer errPwdCount) {
		this.errPwdCount = errPwdCount;
	}

	public Date getReleaseTime() {
		return releaseTime;
	}

	public void setReleaseTime(Date releaseTime) {
		this.releaseTime = releaseTime;
	}

	public Boolean getIsNeedPasswd() {
		return isNeedPasswd;
	}

	public void setIsNeedPasswd(Boolean isNeedPasswd) {
		this.isNeedPasswd = isNeedPasswd;
	}

}
