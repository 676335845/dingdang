public class OrgDataModel implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5243384535246021220L;
	private String id;
	private String type;
	private String name;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
