package me.ywork.message;

import me.ywork.message.base.AbstractDingMessage;

/**
 * 文本消息
 * @author TangGang  2015年8月3日
 *
 */
public class DingTextMessage extends AbstractDingMessage {
	private static final long serialVersionUID = -6828976013849016244L;


	/**
	 * 必填，消息描述
	 */
	private String content;
	
	public DingTextMessage(String content) {
		super();
		
		this.content = content;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
	
	
}
