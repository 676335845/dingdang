package me.ywork.message;

import me.ywork.message.base.AbstractDingMessage;

/**
 * 媒体消息
 * @author TangGang  2015年8月3日
 *
 */
public class DingMediaMessage extends AbstractDingMessage {
	private static final long serialVersionUID = -6828976013849016244L;

	/**
	 * 必填，消息描述
	 */
	private String media_id;
	
	public DingMediaMessage(String media_id) {
		super();
		
		this.setMedia_id(media_id);
	}

	public String getMedia_id() {
		return media_id;
	}

	public void setMedia_id(String media_id) {
		this.media_id = media_id;
	}
	
}
