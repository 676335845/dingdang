package me.ywork.message.base;

import java.io.Serializable;

/**
 * 
 * @author TangGang  2015年8月3日
 *
 */
public abstract class AbstractDingMessageResult implements Serializable {
	private static final long serialVersionUID = -8282837221309183545L;

	private int errcode;
    private String errmsg;
    
    public AbstractDingMessageResult() {
    	
    }

	public int getErrcode() {
		return errcode;
	}

	public void setErrcode(int errcode) {
		this.errcode = errcode;
	}

	public String getErrmsg() {
		return errmsg;
	}

	public void setErrmsg(String errmsg) {
		this.errmsg = errmsg;
	}
    
}
