package me.ywork.kfkmessage;

import com.alibaba.fastjson.JSONObject;
import com.heyi.framework.messagebus.message.DefaultMessage;

public class DingHttpPostMessage extends DefaultMessage {
	/**
	 * 
	 */
	private static final long serialVersionUID = 7984787331916559395L;
	
	private String dingUrl;
	
	private JSONObject jsonObject;
	
	private String corpId;
	
	private String suiteId;

	public JSONObject getJsonObject() {
		return jsonObject;
	}

	public void setJsonObject(JSONObject jsonObject) {
		this.jsonObject = jsonObject;
	}

	public String getDingUrl() {
		return dingUrl;
	}

	public void setDingUrl(String dingUrl) {
		this.dingUrl = dingUrl;
	}

	public String getCorpId() {
		return corpId;
	}

	public void setCorpId(String corpId) {
		this.corpId = corpId;
	}

	public String getSuiteId() {
		return suiteId;
	}

	public void setSuiteId(String suiteId) {
		this.suiteId = suiteId;
	}
}
